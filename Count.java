public class Count {
    public static void main(String[] args) {
        int age = 18;
        int num = 2;
        // 以下为两个变量的 加 减 乘 除 取余 自增
        System.out.println(age + num);
        System.out.println(age - num);
        System.out.println(age * num);
        System.out.println(age / num);
        System.out.println(age % num);
        System.out.println(age % (num * 2));
        System.out.println(age++);
        System.out.println(++num);
    }
}
